package com.kas.clientservice.haiyansmartenforce.Entity;

public class CurrentTime{
        public int mYear;
        public int mMonth;
        public int mDay;
        public int mWay;
        public int mHour;
        public int mMinute;

        public int getmDay() {
            return mDay;
        }

        public void setmDay(int mDay) {
            this.mDay = mDay;
        }

        public int getmHour() {
            return mHour;
        }

        public void setmHour(int mHour) {
            this.mHour = mHour;
        }

        public int getmMinute() {
            return mMinute;
        }

        public void setmMinute(int mMinute) {
            this.mMinute = mMinute;
        }

        public int getmMonth() {
            return mMonth;
        }

        public void setmMonth(int mMonth) {
            this.mMonth = mMonth;
        }

        public int getmWay() {
            return mWay;
        }

        public void setmWay(int mWay) {
            this.mWay = mWay;
        }

        public int getmYear() {
            return mYear;
        }

        public void setmYear(int mYear) {
            this.mYear = mYear;
        }
    }